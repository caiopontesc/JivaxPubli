package br.com.sankhya.domain;

public class TotalFixedBudget {

	private double Descontos;
	private double Encargos;
	private double Honorarios;
	private double Internos;
	private double Iss;
	private double Terceiros;
	private double Total;

	public double getDescontos() {
		return Descontos;
	}

	public void setDescontos(double descontos) {
		Descontos = descontos;
	}

	public double getEncargos() {
		return Encargos;
	}

	public void setEncargos(double encargos) {
		Encargos = encargos;
	}

	public double getHonorarios() {
		return Honorarios;
	}

	public void setHonorarios(double honorarios) {
		Honorarios = honorarios;
	}

	public double getInternos() {
		return Internos;
	}

	public void setInternos(double internos) {
		Internos = internos;
	}

	public double getIss() {
		return Iss;
	}

	public void setIss(double iss) {
		Iss = iss;
	}

	public double getTerceiros() {
		return Terceiros;
	}

	public void setTerceiros(double terceiros) {
		Terceiros = terceiros;
	}

	public double getTotal() {
		return Total;
	}

	public void setTotal(double total) {
		Total = total;
	}

	public TotalFixedBudget() {

	}

}
